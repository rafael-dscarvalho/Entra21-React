


let gerarConvite = function(nomeConvidado, data, horario, local, nomeNoivos){

    return `Caro(a) ${nomeConvidado}, você está sendo convidado para o casamento de ${nomeNoivos}, dia ${data} às ${horario}, no ${local}. Esperamos por você lá!`
}

console.log(gerarConvite("Beyoncé", "15/10/2023", "15:00", "Igreja da Aparecida", "Ana & Paulo"))
console.log(gerarConvite("Jay-Z", "15/10/2023", "15:00", "Igreja da Aparecida", "Ana & Paulo"))
console.log(gerarConvite("Taylor Swift", "15/10/2023", "15:00", "Igreja da Aparecida", "Ana & Paulo"))